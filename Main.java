

// Import the HashSet class
import java.util.HashSet;

public class Main {
  public static void main(String[] args) {
    HashSet<String> flowers = new HashSet<String>();
    flowers.add("Rose");
    flowers.add("Tulip");
    flowers.add("Daisy");
    flowers.add("Dahlia");
    flowers.add("Daffodil");
    System.out.println(flowers);
  }
}
